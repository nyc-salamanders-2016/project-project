class NewTeam extends React.Component {
	constructor() {
		super()
		this.handleSubmit = this.handleSubmit.bind(this)
	}

	handleSubmit(event) {
		event.preventDefault()
		$.ajax({
			url: event.target.action,
			method: event.target.method,
			data: $(event.target).serialize()
		}).done((response) => {
			this.props.onUpdate(response)
			this.refs.name.value = ""
			this.refs.teamLead.value = ""
		  this.refs.pitchID.value = ""		
		})
	}

	render() {
		return(
			<div>
			<h2> Create Team </h2>
			<form onSubmit={this.handleSubmit} action="/teams" method="post">
				<input ref="name" type="text" name="name" placeholder="team name" />
				<input ref="teamLead" type="text" name="team_leader" placeholder="team lead"/>
				<input ref="pitchID" type="number" name="pitch_id" placeholder="Pitch ID"/>
				<input type="submit" value="Create!"/>
			</form>
			</div>
		)
	}

}